package hr.fer.zemris.java.gui.layouts;

public class RCPosition {
	final private int row;
	final private int column;
	
	public RCPosition(int row, int column) {
		this.row = row;
		this.column = column;
	}

	public int getRow() {
		return row;
	}

	public int getColumn() {
		return column;
	}
	
	
}
